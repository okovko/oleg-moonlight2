#include <string.h>

#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*map;
	size_t	i;
	size_t	len;

	if (!s || !f)
		return (NULL);
	i = 0;
	len = ft_strlen(s);
	if ((map = ft_strnew(len)))
	{
		while (i < len)
		{
			map[i] = f(i, s[i]);
			i++;
		}
		map[len] = '\0';
		return (map);
	}
	return (NULL);
}
